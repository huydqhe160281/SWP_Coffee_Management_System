    /** 
     * Handles the HTTP <code>GET</code> method.
     * @param request servlet request
     * @param response servlet response
     * @throws ServletException if a servlet-specific error occurs
     * @throws IOException if an I/O error occurs
     */
    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
    throws ServletException, IOException {
        Map<String, String> fields = new HashMap<>();
        for (String param : request.getParameterMap().keySet()) {
            fields.put(param, request.getParameter(param));
        }

        String secureHash = request.getParameter("vnp_SecureHash");
        fields.remove("vnp_SecureHash");  // Remove the secure hash parameter before computing

        String computedHash = ConfigVNPay.hashAllFields(fields);

        if (secureHash.equals(computedHash)) {
            // Process logic after successful payment
            response.getWriter().write("Payment Success!");
        } else {
            response.getWriter().write("Invalid Payment!");
        }
    } 

